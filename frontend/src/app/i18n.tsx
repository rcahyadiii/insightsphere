type Language = "ID" | "EN";

/** Key-value pairs for string interpolation, e.g. { page: 3, name: "Faiz" } */
type Params = Record<string, string | number>;

export interface I18nContextType {
  lang: Language;
  setLang: (lang: Language) => void;
  t: (key: string, params?: Params) => string;
}

export const I18nContext = createContext<I18nContextType | undefined>(undefined);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLang] = useState<Language>("ID");

  const t = (key: string, params?: Params): string => {
    const raw = translations[lang]?.[key];

    if (raw === undefined) {
      if (process.env.NODE_ENV === "development") {
        console.warn(`[i18n] Missing key "${key}" in locale "${lang}"`);
      }
      return key;
    }

    if (!params) return raw;

    return Object.entries(params).reduce<string>(
      (str, [k, v]) => str.replaceAll(`{${k}}`, String(v)),
      raw
    );
  };

  return (
    <I18nContext.Provider value={{ lang, setLang, t }}>
      {children}
    </I18nContext.Provider>
  );
}

export const useTranslation = () => {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error("useTranslation must be used within an I18nProvider");
  }
  return context;
};

/**
 * Scoped translation hook — auto-prefixes every key with `prefix`.
 *
 * @example
 * const { t } = useTranslations("cm");
 * t("header")        // → resolves "cm.header"
 * t("kpi.balance")   // → resolves "cm.kpi.balance"
 * t("pagination.page", { page: 3 }) // → resolves "cm.pagination.page" with interpolation
 */
export const useTranslations = (prefix: string) => {
  const { t, lang, setLang } = useTranslation();
  return {
    t: (key: string, params?: Params) => t(`${prefix}.${key}`, params),
    lang,
    setLang,
  };
};
